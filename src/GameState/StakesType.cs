/// <summary>
/// The stakes involved in a relationship or obligation
/// Higher stakes create more tension and urgency in conversations
/// </summary>
public enum StakesType
{
None,           // No particular stakes
Reputation,     // Social standing at risk
Wealth,         // Financial consequences  
Safety,         // Physical or emotional safety threatened
Love,           // Romantic or familial bonds at stake
Honor,          // Personal integrity or vows
Freedom,        // Autonomy or independence threatened
Knowledge,      // Important secrets or information
Power,          // Political influence or authority
Soul            // Spiritual or existential stakes
}