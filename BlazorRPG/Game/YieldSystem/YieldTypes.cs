public enum YieldTypes
{
    Resource,                   // Direct resource gain (food, energy, etc.)
    SkillXP,                   // Skill experience points
    LocationAccess,            // Unlock new locations
    LocationSpotAccess,       // Unlock new spots within locations
    ActionUnlock,              // Unlock new actions
    CardUnlock,                // Unlock new cards for encounters
    EfficiencyBoost,           // Improve yield rates for specific action types
    TravelDiscount,            // Reduce travel costs to specific locations
    EncounterChanceReduction, // Reduce chance of encounters
    NodeDiscovery,             // Discover aspects of resource nodes
    NodeReplenish              // Restore depleted resource nodes
}

