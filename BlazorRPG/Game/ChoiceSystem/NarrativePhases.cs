
/// <summary>
/// Phases of narrative progression during an encounter
/// </summary>
public enum NarrativePhases
{
    Introduction,       // Low momentum, low pressure
    Complication,       // Rising pressure
    Escalation,         // High pressure relative to momentum
    Breakthrough,       // Momentum opportunity during high pressure
    Resolution,         // High momentum, manageable pressure
    Crisis              // Both high momentum and pressure
}